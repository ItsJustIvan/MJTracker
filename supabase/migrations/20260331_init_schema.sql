-- New Function: Undo Logic (Added to the core schema)
CREATE OR REPLACE FUNCTION undo_last_transaction(p_session_id UUID)
RETURNS void AS $$
DECLARE
    v_last_tx_id BIGINT;
    v_prev_dealer_idx INT;
BEGIN
    SELECT id, dealer_at_time INTO v_last_tx_id, v_prev_dealer_idx
    FROM public.transactions
    WHERE session_id = p_session_id
    ORDER BY created_at DESC
    LIMIT 1;

    IF v_last_tx_id IS NOT NULL THEN
        DELETE FROM public.transactions WHERE id = v_last_tx_id;
        UPDATE public.sessions 
        SET current_dealer_idx = v_prev_dealer_idx,
            dealer_streak = GREATEST(0, dealer_streak - 1)
        WHERE id = p_session_id;
    END IF;
END;
$$ LANGUAGE plpgsql;